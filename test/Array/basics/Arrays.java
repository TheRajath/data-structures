package Array.basics;

public class Arrays {

    int[] initializeSortedArray(int size) {

        int[] array = new int[size];

        array[0] = 10;
        array[1] = 20;
        array[2] = 30;
        array[3] = 40;
        array[4] = 50;

        return array;
    }

    int[] initializeUnsortedArray(int size) {

        int[] array = new int[size];

        array[0] = 50;
        array[1] = 40;
        array[2] = 90;
        array[3] = 60;
        array[4] = 70;

        return array;
    }

}
